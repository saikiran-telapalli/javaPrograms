package Interview;

import java.util.Scanner;

public class PrintCubeWithoutMultiply {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int num = scan.nextInt();
        int result=0;
        for(int i=0; i<num; i++){
            for(int j=0; j<num ;j++){
                result += num;
            }
        }
        System.out.println(result);
    }
}
